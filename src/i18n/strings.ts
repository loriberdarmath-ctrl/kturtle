const DICTS: Record<Locale, Dict> = { en, ru, hy };

/** Interpolate {0}, {1} tokens. Missing keys fall back to English, then key. */
export function translate(locale: Locale, key: string, ...args: (string | number)[]): string {
  const template = DICTS[locale]?.[key] ?? DICTS.en[key] ?? key;
  if (args.length === 0) return template;
  return template.replace(/\{(\d+)\}/g, (_, i) => String(args[Number(i)] ?? ''));
}
